// exemplos de uso switch

let idade = 5;

switch (idade) {
    case 1, 5:
        console.log("cadastro");
        break;
    case 2:
        console.log("consulta");
        break;
    case 3:
        console.log("alteração");
        break;
    case 4:
        console.log("exclusão");
        break;
    default:
        console.log("i'm a teapot");
        break;
}